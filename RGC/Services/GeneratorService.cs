        public static string GenerateDescription(string? projectName = null)
        {
            var category = Pick(DescTemplates);
            var tmpl = Pick(category);
            var name = projectName ?? GenerateName();
            return string.Format(tmpl, name);
        }

        public static string GeneratePassword(int length = 16, bool special = true)
        {
            const string upper = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
            const string lower = "abcdefghijklmnopqrstuvwxyz";
            const string digits = "0123456789";
            const string sym = "!@#$%^&*()_+-=[]{}|;:,.<>?";

            var chars = upper + lower + digits;
            if (special) chars += sym;

            var data = RandomNumberGenerator.GetBytes(length);
            var result = new char[length];
            for (var i = 0; i < length; i++)
                result[i] = chars[data[i] % chars.Length];

            return new string(result);
        }

        private static T Pick<T>(T[] items) => items[RandomNumberGenerator.GetInt32(items.Length)];
